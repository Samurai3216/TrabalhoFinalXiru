    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        try {
            // Verificação de campos obrigatórios
            if (txtNomeProduto.getText().isEmpty()
                    || txtCategoria.getText().isEmpty()
                    || txtValor.getText().isEmpty()
                    || txtQtdeEstoque.getText().isEmpty()
                    || txtDataCadastro.getText().isEmpty()) {

                JOptionPane.showMessageDialog(this, "Por favor, preencha todos os campos antes de salvar o produto!", "Campos obrigatórios", JOptionPane.WARNING_MESSAGE);
                return;
            }

            // Validação do nome do produto
            String nomeProduto = txtNomeProduto.getText();
            if (!validarNomeProduto(nomeProduto)) {
                return; // Interrompe se nome for inválido
            }

            // Criação do objeto Produto
            Produto p = new Produto();
            p.setNomeProd(nomeProduto);
            p.setCategoriaProd(txtCategoria.getText());

            // Conversão segura do valor
            try {
                String valorTexto = txtValor.getText().replace(",", ".");
                double valor = Double.parseDouble(valorTexto);
                p.setValorUnitProd(valor);
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(this, "Preço inválido. Digite um valor numérico.", "Erro de preço", JOptionPane.WARNING_MESSAGE);
                return;
            }

            // Conversão segura da quantidade
            try {
                int qtde = Integer.parseInt(txtQtdeEstoque.getText());
                p.setQuantidadeProd(qtde);
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(this, "Estoque inválido. Digite um número inteiro.", "Erro de estoque", JOptionPane.WARNING_MESSAGE);
                return;
            }

            p.setDataCadProd(txtDataCadastro.getText());

            // Salvar no banco
            ProdutoDAO pd = new ProdutoDAO();
            pd.SalvarProduto(p);

            JOptionPane.showMessageDialog(this, "Produto salvo com sucesso!");
            this.dispose();

        } catch (SQLException ex) {
            Logger.getLogger(cadProduto.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, "Erro ao salvar o produto no banco de dados.", "Erro", JOptionPane.ERROR_MESSAGE);
        }


    }//GEN-LAST:event_btnSalvarActionPerformed

