    public boolean validarNomeProduto(String nome) {
    if (nome.length() < 5) {
        JOptionPane.showMessageDialog(this, "O nome do produto deve ter no mínimo 5 caracteres.");
        return false;
    }

    if (!nome.matches("^[A-Za-zÀ-ÿ\\s]+$")) {
        JOptionPane.showMessageDialog(this, "O nome do produto não pode conter números ou caracteres especiais.");
        return false;
    }

    return true;
}
    
